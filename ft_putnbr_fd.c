#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		write (fd, "-2147483648", 11);
	}
	if (n < 0 && n != -2147483648)
	{
		write (fd, "-", 1);
		n = -n;
	}
	if (n >= 10 && n != -2147483648)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else if (n <= 10 && n != -2147483648)
	{
		n = n + 48;
		write(fd, &n, 1);
	}
}
